def _get_python() -> str:
    if os.name == "nt":
        local_app = Path(os.environ.get("LOCALAPPDATA", Path.home() / "AppData" / "Local"))
        home      = Path.home()
        venv_scripts = [
            local_app / "pipx" / "venvs" / "zettabrain-rag" / "Scripts",
            home / ".local" / "pipx" / "venvs" / "zettabrain-rag" / "Scripts",
        ]
        candidates = [Path(sys.executable)] + [
            p for d in venv_scripts for p in d.glob("python*.exe") if p.exists()
        ]
    else:
        candidates = [
            *list(Path("/root/.local/share/pipx/venvs/zettabrain-rag").rglob("python3")),
            *list(Path("/opt/zettabrain/venv/bin").glob("python3")),
            Path(sys.executable),
        ]
    for c in candidates:
        if Path(c).exists():
            r = subprocess.run([str(c), "-c", "import langchain_community"],
                               capture_output=True)
            if r.returncode == 0:
                return str(c)
    return sys.executable

def _ollama_running() -> bool:
    """Check Ollama is reachable — always use http:// internally."""
    _url = OLLAMA_URL.replace("https://", "http://")  # Ollama API is never TLS
    try:
        r = requests.get(_url, timeout=5)
        return r.status_code == 200
    except requests.exceptions.ConnectionError:
        return False
    except Exception:
        return False

def _get_chunk_count() -> int:
    try:
        import chromadb
        client = chromadb.PersistentClient(path=str(CHROMA_PATH))
        return client.get_collection("zettabrain_docs").count()
    except Exception:
        return 0

def _get_sources() -> list:
    if not INGEST_LOG.exists():
        return []
    try:
        data = json.loads(INGEST_LOG.read_text(encoding="utf-8"))
        return sorted([Path(p).name for p in data.keys()])
    except Exception:
        return []

def _get_ollama_models() -> list:
    try:
        r = requests.get(f"{OLLAMA_URL}/api/tags", timeout=5)
        if r.status_code == 200:
            return [m["name"] for m in r.json().get("models", [])]
    except Exception:
        pass
    return []

def _get_tls_info() -> dict:
    cert = Path(CERT_FILE)
    return {
        "enabled":     cert.exists(),
        "cert_file":   CERT_FILE,
        "fingerprint": FINGERPRINT,
    }

def _get_storage_sources() -> list[dict]:
    """Return all configured storage sources from storage.conf."""
    sources = []
    if STORAGE_CONF.exists():
        for line in STORAGE_CONF.read_text(encoding="utf-8").splitlines():
            if line.startswith("#") or not line.strip():
                continue
            parts = line.split("|")
            if len(parts) >= 4:
                sources.append({
                    "role":  parts[0].strip(),
                    "type":  parts[1].strip(),
                    "label": parts[2].strip(),
                    "path":  parts[3].strip(),
                })
    # Fall back to primary DOCS_FOLDER if no conf file
    if not sources:
        sources.append({"role": "primary", "type": STORAGE_TYPE,
                        "label": DOCS_FOLDER, "path": DOCS_FOLDER})
    return sources

def _count_docs_all_sources() -> int:
    """Count supported documents across every configured storage path."""
    total = 0
    seen  = set()
    for src in _get_storage_sources():
        p = Path(src["path"])
        if p in seen or not p.exists():
            continue
        seen.add(p)
        for ext in ["*.pdf", "*.txt", "*.docx", "*.md"]:
            total += len(list(p.rglob(ext)))
    return total



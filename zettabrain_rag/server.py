@app.post("/api/ingest")
async def ingest(req: IngestRequest):
    python = _get_python()
    script = DEPLOY_DIR / "05_ingest_documents.py"
    if not script.exists():
        raise HTTPException(status_code=404, detail=f"Ingest script not found: {script}")

    cmd = [python, str(script)]
    if req.folder:  cmd += ["--folder", req.folder]
    if req.rebuild: cmd += ["--rebuild"]

    try:
        result = subprocess.run(cmd, cwd=str(DEPLOY_DIR),
                                capture_output=True, text=True, timeout=600)
        return {
            "success": result.returncode == 0,
            "output":  result.stdout,
            "error":   result.stderr if result.returncode != 0 else None,
            "chunks":  _get_chunk_count(),
        }
    except subprocess.TimeoutExpired:
        raise HTTPException(status_code=408, detail="Ingestion timed out.")
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@app.post("/api/chat")
async def chat(req: ChatRequest):
    if not _ollama_running():
        raise HTTPException(status_code=503, detail="Ollama is not running.")
    if _get_chunk_count() == 0:
        raise HTTPException(status_code=422, detail="Vector store is empty. Run ingestion first.")

    model = req.model or LLM_MODEL
    try:
        from langchain_ollama import OllamaEmbeddings, OllamaLLM
        from langchain_chroma import Chroma
        from langchain_core.prompts import PromptTemplate
        from langchain_core.runnables import RunnablePassthrough
        from langchain_core.output_parsers import StrOutputParser

        embeddings  = OllamaEmbeddings(model=EMBED_MODEL, base_url=OLLAMA_URL)
        vectorstore = Chroma(persist_directory=str(CHROMA_PATH),
                             embedding_function=embeddings,
                             collection_name="zettabrain_docs")
        retriever   = vectorstore.as_retriever(
            search_type="mmr",
            search_kwargs={"k": 6, "fetch_k": 20, "lambda_mult": 0.7}
        )
        prompt = PromptTemplate.from_template(
            "You are ZettaBrain. Answer using the context below.\n\n"
            "CONTEXT:\n{context}\n\nQUESTION: {question}\n\nANSWER:"
        )
        def format_docs(docs):
            return "\n\n---\n\n".join(
                f"[Source: {Path(d.metadata.get('source','?')).name}]\n{d.page_content}"
                for d in docs
            )

        llm    = OllamaLLM(model=model, base_url=OLLAMA_URL, temperature=0.0, num_predict=1024)
        sources = retriever.invoke(req.question)
        chain   = ({"context": retriever | format_docs, "question": RunnablePassthrough()}
                   | prompt | llm | StrOutputParser())
        answer  = chain.invoke(req.question)

        return {
            "answer":         answer,
            "model":          model,
            "chunks_searched": len(sources),
            "sources": [
                {"filename": Path(s.metadata.get("source","?")).name,
                 "page":     s.metadata.get("page",""),
                 "preview":  s.page_content[:200]}
                for s in sources
            ],
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@app.websocket("/ws/chat")
async def websocket_chat(websocket: WebSocket):
    await websocket.accept()
    try:
        while True:
            data     = await websocket.receive_text()
            payload  = json.loads(data)
            question = payload.get("question", "").strip()
            model    = payload.get("model", LLM_MODEL)

            if not question:
                await websocket.send_json({"type": "error", "message": "Empty question"})
                continue
            if not _ollama_running():
                await websocket.send_json({"type": "error", "message": "Ollama is not running"})
                continue
            if _get_chunk_count() == 0:
                await websocket.send_json({"type": "error", "message": "Vector store is empty"})
                continue

            try:
                from langchain_ollama import OllamaEmbeddings
                from langchain_chroma import Chroma

                embeddings  = OllamaEmbeddings(model=EMBED_MODEL, base_url=OLLAMA_URL)
                vectorstore = Chroma(persist_directory=str(CHROMA_PATH),
                                     embedding_function=embeddings,
                                     collection_name="zettabrain_docs")
                retriever   = vectorstore.as_retriever(
                    search_type="mmr",
                    search_kwargs={"k": 6, "fetch_k": 20, "lambda_mult": 0.7}
                )

                sources     = retriever.invoke(question)
                source_list = [
                    {"filename": Path(s.metadata.get("source","?")).name,
                     "page":     s.metadata.get("page",""),
                     "preview":  s.page_content[:200]}
                    for s in sources
                ]
                await websocket.send_json({"type": "sources", "sources": source_list})

                context     = "\n\n---\n\n".join(
                    f"[Source: {Path(s.metadata.get('source','?')).name}]\n{s.page_content}"
                    for s in sources
                )
                prompt_text = (
                    "You are ZettaBrain. Answer using the context below.\n\n"
                    f"CONTEXT:\n{context}\n\n"
                    f"QUESTION: {question}\n\nANSWER:"
                )

                import asyncio

                loop  = asyncio.get_event_loop()
                queue: asyncio.Queue = asyncio.Queue()

                def _stream_ollama():
                    try:
                        resp = requests.post(
                            f"{OLLAMA_URL}/api/generate",
                            json={"model": model, "prompt": prompt_text, "stream": True},
                            stream=True, timeout=120,
                        )
                        if resp.status_code != 200:
                            asyncio.run_coroutine_threadsafe(
                                queue.put(("error", f"Ollama {resp.status_code}: {resp.text[:300]}")), loop
                            )
                            return
                        for line in resp.iter_lines():
                            if line:
                                chunk = json.loads(line)
                                if "error" in chunk:
                                    asyncio.run_coroutine_threadsafe(
                                        queue.put(("error", f"Ollama: {chunk['error']}")), loop
                                    )
                                    return
                                token = chunk.get("response", "")
                                asyncio.run_coroutine_threadsafe(
                                    queue.put(("token", token)), loop
                                )
                                if chunk.get("done"):
                                    break
                    except Exception as exc:
                        asyncio.run_coroutine_threadsafe(
                            queue.put(("error", str(exc))), loop
                        )
                    finally:
                        asyncio.run_coroutine_threadsafe(
                            queue.put(("done", None)), loop
                        )

                loop.run_in_executor(None, _stream_ollama)

                full_answer = ""
                error_msg   = None
                while True:
                    kind, value = await queue.get()
                    if kind == "token":
                        full_answer += value
                        await websocket.send_json({"type": "token", "token": value})
                    elif kind == "error":
                        error_msg = value
                        break
                    else:  # "done"
                        break

                if error_msg:
                    await websocket.send_json({"type": "error", "message": error_msg})
                    continue

                if not full_answer.strip():
                    await websocket.send_json({
                        "type":    "error",
                        "message": f"Ollama returned an empty response. "
                                   f"Check that '{model}' is pulled: ollama pull {model}",
                    })
                    continue

                await websocket.send_json({
                    "type":            "done",
                    "answer":          full_answer,
                    "model":           model,
                    "chunks_searched": len(sources),
                })

            except Exception as e:
                await websocket.send_json({"type": "error", "message": str(e)})

    except WebSocketDisconnect:
        pass


@app.delete("/api/vectorstore")
async def clear_vectorstore():
    try:
        import chromadb
        chromadb.PersistentClient(path=str(CHROMA_PATH)).delete_collection("zettabrain_docs")
        if INGEST_LOG.exists():
            INGEST_LOG.write_text("{}")
        return {"success": True, "message": "Vector store cleared."}
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

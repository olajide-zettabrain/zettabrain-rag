@app.get("/api/status")
async def status():
    chunks          = _get_chunk_count()
    ingested        = _get_sources()
    models          = _get_ollama_models()
    storage_sources = _get_storage_sources()
    doc_count       = _count_docs_all_sources()

    return {
        "ollama": {
            "running":      _ollama_running(),
            "url":          OLLAMA_URL,
            "models":       models,
            "active_llm":   LLM_MODEL,
            "active_embed": EMBED_MODEL,
        },
        "vectorstore": {
            "exists": CHROMA_PATH.exists(),
            "chunks": chunks,
            "path":   str(CHROMA_PATH),
        },
        "storage": {
            "type":        STORAGE_TYPE,
            "path":        DOCS_FOLDER,
            "doc_count":   doc_count,
            "configured":  any(Path(s["path"]).exists() for s in storage_sources),
            "sources":     storage_sources,
        },
        "tls":     _get_tls_info(),
        "sources": ingested,
    }


